document.getElementById('emailForm').addEventListener('Submit', function(event) {
    event.preventDefault(); 
    
    let email = document.getElementById('email').value;
    let errorElement = document.getElementById('error');
    let errorMessage = '';
    // let errorMessage инициализируется пустой строкой для хранения сообщения об ошибке, если оно будет.

    let emailRegex = /^[^@]+@[^@]+\.[^@]+$/;

    if (!emailRegex.test(email)) {
        errorMessage = 'Неверный формат email. Убедитесь, что email содержит несколько символов до и после "@", а также точку после "@".';
    }

    if (errorMessage) {
        errorElement.textContent = errorMessage;
    } else {
        errorElement.textContent = '';
        alert('Email введен корректно!')
    }
});



// Регулярное выражение: ^[^@]+@[^@]+\.[^@]+$
// Представим, что мы разбираем головоломку. Эта головоломка помогает нам понять, правильно ли написан email. Мы будем идти по частям, как будто собираем пазл.

// 1. Начало строки: ^
// Этот символ (^) говорит нам, что мы начинаем проверку с самого начала email.
// 2. Несколько символов до @: [^@]+
// [ ] — это как коробка, в которую мы можем положить любые символы.
// ^@ внутри коробки говорит нам, что мы не хотим символ @ внутри этой части.
// + говорит, что мы хотим один или больше таких символов.
// Это значит, что первая часть email должна содержать хотя бы один символ, который не является @.

// 3. Символ @: @
// Этот символ (@) говорит, что тут обязательно должен быть символ @.
// 4. Несколько символов после @ и до точки: [^@]+
// Снова [ ] — это коробка, и ^@ внутри говорит, что мы не хотим символ @ в этой части.
// + опять говорит, что мы хотим один или больше таких символов.
// Это значит, что после @ должно быть хотя бы одно слово, которое не содержит @.

// 5. Точка: \.
// Этот символ (\.) говорит, что здесь должна быть точка ..
// 6. Несколько символов после точки: [^@]+
// [ ] — это коробка, и ^@ внутри снова говорит, что мы не хотим символ @ в этой части.
// + говорит, что мы хотим один или больше таких символов.
// Это значит, что после точки должно быть хотя бы одно слово, которое не содержит @.

// 7. Конец строки: $
// Этот символ ($) говорит нам, что проверка заканчивается здесь. Это конец email.
// Объединяем все вместе
// Вот что мы проверяем:

// Email должен начинаться с одного или нескольких символов, кроме @.
// Потом должен быть символ @.
// После @ должно быть одно или несколько слов, которые не содержат @.
// Потом должна быть точка ..
// После точки должно быть одно или несколько слов, которые не содержат @.
// И это должно быть концом email.